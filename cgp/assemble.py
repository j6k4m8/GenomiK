def find_max(a, b, c, d):
  temp = [a, b, c, d]
  var_map = {0: 'A', 1: 'C', 2: 'G', 3: 'T'}
  max_occ = max(temp)
  matches = []
  for x in range(len(temp)):
    if temp[x] == max_occ:
      matches.append(x)
  if max_occ == 0 or len(matches) > 1:
    return('N')
  else:
    return(var_map[matches[0]])

